			<!-- 分割线 -->
			<split></split>
			<ratingselect :selectType="selectType" :onlyContent="onlyContent" :ratings="ratings" @select="select" @toggle="toggle"></ratingselect>
			<div class="rating-wrapper">
				<ul>
					<li v-show="needShow(rating.rateType,rating.text)" v-for="rating in ratings" class="rating-item border-1px">
						<!-- 头像 -->
						<div class="avatar">
							<img :src="rating.avatar" width="28" height="28">
						</div>
						<!-- 内容 -->
						<div class="content">
							<!-- 用户名 -->
							<div class="name">{{ rating.username }}</div>
							<div class="star-wrapper">
								<star class="star" :size="24" :score="rating.score"></star>
								<span class="delivery" v-show="rating.deliveryTime">{{ rating.deliveryTime }}分钟送达</span>
							</div>
							<p class="text">{{ rating.text }}</p>
							<!-- 推荐 -->
							<div class="recommend" v-show="rating.recommend && rating.recommend.length">
								<i class="icon-thumb_up"></i>
								<span class="item" v-for="item in rating.recommend">{{ item }}</span>
							</div>
							<!-- 评价时间 -->
							<div class="time">
								{{ rating.rateTime | formatDate }}
							</div>
						</div>
					</li>
				</ul>
			</div>
		</div>
	</div>
</template>

<script type="ecmascript-6">
	import BScroll from 'better-scroll'
	import star from '../star/star.vue'
	import ratingselect from '../ratingselect/ratingselect.vue'
	import split from '../split/split.vue'
	import bus from '../../common/js/bus.js'
	import { formatDate } from '../../common/js/date.js'

	const POSITIVE = 0   // 好的评价
	const NEGATIVE = 1   // 坏的评价
	const ALL = 2        // 所有评价
	const ERR_OK = 0

	export default {
		props: {
			seller: Object
		},
		data() {
			return {
				ratings: [],
				selectType: ALL,
				onlyContent: true
			}
		},
		created() {
			this.$http.get('/api/ratings').then((res) => {
				res = res.body
				if (res.errno === ERR_OK) {
					this.ratings = res.data
					this.$nextTick(() => {
						this.scroll = new BScroll(this.$refs.ratings,{
							click: true
						})
					})
				}
			})
			/*// 监听来自ratingselect.vue的改变selectType值的 “select” 函数
			bus.$on('select',(type) => {
				this.selectType = type
				this.$nextTick(() => {
					this.scroll.refresh()
				})
			}),
			// 监听来自ratingselect.vue的改变onlyContent值的 “toggle” 函数
			bus.$on('toggle',() => {
				this.onlyContent = !this.onlyContent
				this.$nextTick(() => {
					this.scroll.refresh()
				})
			})*/
		},
		methods: {
			needShow(type,text) {
				if (this.onlyContent && !text) {
					return false
				}
				if (this.selectType === ALL) {
					return true
				}else{
					return type === this.selectType
				}
			},
			// 监听来自ratingselect.vue的改变selectType值的 “select” 函数
			select(type) {
				this.selectType = type
				this.$nextTick(() => {
					this.scroll.refresh()
				})
			},
			// 监听来自ratingselect.vue的改变onlyContent值的 “toggle” 函数
			toggle() {
				this.onlyContent = !this.onlyContent
				this.$nextTick(() => {
					this.scroll.refresh()
				})
			}
		},
		filters: {
			formatDate(time) {
				let date = new Date(time)
				return formatDate(date,'yyyy-MM-dd hh:mm')
			}
		},
		components: {
			star,
			ratingselect,
			split
		}
	}
</script>

